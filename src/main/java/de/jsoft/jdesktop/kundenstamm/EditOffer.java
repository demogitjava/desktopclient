    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        model = (DefaultTableModel) jTable3.getModel();

        int row = jTable3.getSelectedRow();

        Integer anzahl = (Integer) jTable3.getModel().getValueAt(row, 3);
        if(anzahl == null)
        {
            jTextField1.setText(String.valueOf(1));
        }
        else
        {
            jTextField1.setText(String.valueOf(jTable3.getModel().getValueAt(row, 3)));
        }

        double preis = (double) jTable3.getModel().getValueAt(row, 2);
        jTextField2.setText(String.valueOf(preis));

    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable3.getSelectedRow();

        Integer anzahl = Integer.valueOf(jTextField1.getText());
        Double preis = Double.valueOf(jTextField2.getText());

        model.setValueAt(anzahl, row, 3);  // anzahl menge
        model.setValueAt(preis, row,2);

    }//GEN-LAST:event_jButton2ActionPerformed

